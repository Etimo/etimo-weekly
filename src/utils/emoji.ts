// Custom emoji map (name -> URL or alias)
let customEmojiMap: Record<string, string> = {};

/**
 * Set the custom emoji map (fetched from Slack).
 * Call this before converting text to populate custom emojis.
 */
export function setCustomEmojis(emojis: Record<string, string>): void {
	customEmojiMap = emojis;
	console.log(`  ðŸŽ¨ Loaded ${Object.keys(emojis).length} custom emojis`);
}

/**
 * Convert emoji shortcodes (like :smile:) to Unicode emojis or images.
 *
 * @param text - Text containing emoji shortcodes
 * @returns Text with shortcodes converted to Unicode emojis or img tags
 */
export function convertEmojiShortcodes(text: string): string {
	return text.replace(/:([a-zA-Z0-9_+-]+):/g, (match, shortcode) => {
		const lowerCode = shortcode.toLowerCase();

		// Check standard emoji map first
		const standardEmoji = EMOJI_MAP[lowerCode];
		if (standardEmoji) {
			return standardEmoji;
		}

		// Check custom emojis
		const customEmoji = customEmojiMap[lowerCode];
		if (customEmoji) {
			// Handle aliases (e.g., "alias:thumbsup")
			if (customEmoji.startsWith("alias:")) {
				const aliasTarget = customEmoji.slice(6);
				const aliasEmoji = EMOJI_MAP[aliasTarget];
				return aliasEmoji || match;
			}

			// It's a URL - render as inline image
			return `<img src="${customEmoji}" alt="${shortcode}" class="inline-emoji" style="height: 1.2em; width: 1.2em; vertical-align: middle; display: inline-block;">`;
		}

		// Return original if not found
		return match;
	});
}

/**
 * Process all text content in an object, converting emoji shortcodes.
 * Recursively processes nested objects and arrays.
 */
export function convertEmojisInObject<T>(obj: T): T {
	if (typeof obj === "string") {
		return convertEmojiShortcodes(obj) as T;
	}

	if (Array.isArray(obj)) {
		return obj.map((item) => convertEmojisInObject(item)) as T;
	}

	if (obj !== null && typeof obj === "object") {
		const result: Record<string, unknown> = {};
		for (const [key, value] of Object.entries(obj)) {
			result[key] = convertEmojisInObject(value);
		}
		return result as T;
	}

	return obj;
}
